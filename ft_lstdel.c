#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	if (*alst)
	{
		del((*alst)->content, (*alst)->content_size);
		free(*alst);
		(*alst)->content = NULL;
		del((*alst)->next, (*alst)->content_size);
		free(*alst);
		(*alst)->next = NULL;
	}
}
